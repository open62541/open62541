static void removeDirectory(const char* path)
{
	DIR *dir = opendir(path);
	if(dir) {
        struct dirent *dirent;
	    while((dirent = readdir(dir)) != NULL) {

	    	/* remove regular file */
	        if (dirent->d_type == DT_REG) {
	        	char file_name[FILENAME_MAX];
	            snprintf(file_name, FILENAME_MAX, "%s/%s", path, (char*)dirent->d_name);
	            remove(file_name);
	        }

	        /* remove directory */
	        else if (dirent->d_type == DT_DIR) {
	        	if (strncmp((char*)dirent->d_name, "..", 2) == 0) continue;
	        	if (strncmp((char*)dirent->d_name, ".", 1) == 0) continue;

	        	char directory_name[FILENAME_MAX];
	            snprintf(directory_name, FILENAME_MAX, "%s/%s", path, (char*)dirent->d_name);
	            removeDirectory(directory_name);
	        }
	    }
	    closedir(dir);
	}

	rmdir(path);
}

